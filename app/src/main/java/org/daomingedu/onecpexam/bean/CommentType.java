public class CommentType {
    private String id;
    private String typeName;
    private int orderNo;
    private List<SampleComment> testCommentList;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    public int getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(int orderNo) {
        this.orderNo = orderNo;
    }

    public List<SampleComment> getTestCommentList() {
        return testCommentList;
    }

    public void setTestCommentList(List<SampleComment> testCommentList) {
        this.testCommentList = testCommentList;
    }
}
