public class TestRoomStudent implements Serializable {

    public static final String STATUS_COMPLETE = "2"; //已点评
    public static final String STATUS_PARTICIPATE = "1";//已参考
    public static final String STATUS_NO_PARTICIPATE = "0"; //未参考

    private String testSignId;
    private String studentName;
    private String photoImg;
    private String status;
    private String attendScore;

    public String getTestSignId() {
        return testSignId;
    }

    public void setTestSignId(String testSignId) {
        this.testSignId = testSignId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getPhotoImg() {
        return photoImg;
    }

    public void setPhotoImg(String photoImg) {
        this.photoImg = photoImg;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getAttendScore() {
        return attendScore;
    }

    public void setAttendScore(String attendScore) {
        this.attendScore = attendScore;
    }
}
